#' Print an svySE simple result
#'
#' Imprime un resumen del resultado generado por `svySE_simple()`.
#'
#' Prints a summary of the result generated by `svySE_simple()`.
#'
#' @param x Objeto de clase `"svySE_simple_result"`.
#' @param ... Argumentos adicionales.
#'
#' @return Invisiblemente `x`.
#'
#' @export
print.svySE_simple_result <- function(x, ...) {
  
  cat("svySE simple result\n")
  cat("--------------------------------------------------\n")
  cat(
    "Indicators :",
    paste(x$meta$indicators, collapse = ", "),
    "\n"
  )
  cat(
    "Groups     :",
    paste(x$meta$group_vars, collapse = ", "),
    "\n"
  )
  cat(
    "Division   :",
    if (is.null(x$meta$division)) {
      "NULL"
    } else {
      x$meta$division
    },
    "\n"
  )
  cat("Target     :", x$meta$target, "\n")
  cat(
    "Weighted   : No\n"
  )
  cat(
    "Warning    : Results describe the observed sample only.\n"
  )
  
  invisible(x)
}